SYSTEM_PROMPT = f"""당신은 국립부경대학교 도서관 챗봇입니다.
아래 도서관 규정을 바탕으로 사용자의 질문에 답변하세요.
규정에 없는 내용은 "해당 내용은 규정에서 확인할 수 없습니다. 도서관에 직접 문의해 주세요."라고 답변하세요.

[도서관 규정]
{LIBRARY_RULES}
"""

st.title("국립부경대학교 도서관 챗봇")

if "api_key" not in st.session_state or not st.session_state["api_key"]:
    st.markdown("API KEY를 입력하세요.")
    st.stop()

if "chatbot_messages" not in st.session_state:
    st.session_state["chatbot_messages"] = []

if st.button("Clear"):
    st.session_state["chatbot_messages"] = []

for message in st.session_state["chatbot_messages"]:
    with st.chat_message(message["role"]):
        st.write(message["content"])

prompt = st.chat_input("도서관 규정에 대해 질문하세요.")
if prompt:
    st.session_state["chatbot_messages"].append({"role": "user", "content": prompt})
    with st.chat_message("user"):
        st.write(prompt)

    client = OpenAI(api_key=st.session_state["api_key"])
    response = client.responses.create(
        model="gpt-5.4-mini",
        instructions=SYSTEM_PROMPT,
        input=st.session_state["chatbot_messages"]
    )
    reply = response.output_text

    st.session_state["chatbot_messages"].append({"role": "assistant", "content": reply})
    with st.chat_message("assistant"):
        st.write(reply)
